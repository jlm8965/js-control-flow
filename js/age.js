let i = 7;
for(let i=0; i<=20; i++) {
    if(i<=2)
        console.log("infant is 2 years and younger. You are "+ i + " years old and are an infant.");
    if(i>=3 && i<=5)
        console.log("toddler is 2 - 5 years old. You are " + i + " years old. You are a toddler.");
    if(i>=6 && i<=12)
        console.log("preteen is 6 - 12 years old. You are " + i + " years old. You are a preteen.");
    if(i>=13 && i<=19)
        console.log("teenager is 13 - 19 years old. You are " + i + " years old. You are a teenager.");
    if(i>=20)
        console.log("young adult is 20 years old. You are " + i + " years old. You are a young adult.");
}
